// Check If a Number is Positive, Negative, or Zero
function CheckNumbers(number){
    if(number>0){
        console.log("the given number is positive")
    }else if(number<0){
        console.log("the given number is negative")
    }else{
        console.log("the given number is zero")
    }
}
// Numbers(10)
CheckNumbers(5)
CheckNumbers(-3)
CheckNumbers(0)
